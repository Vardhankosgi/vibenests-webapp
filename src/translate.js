const delay = (ms) => new Promise(resolve => setTimeout(resolve, ms));

async function translateText(text, targetLang) {
  // Safe delay between requests
  await delay(150);

  const url = `https://translate.googleapis.com/translate_a/single?client=gtx&sl=en&tl=${targetLang}&dt=t&q=${encodeURIComponent(text)}`;
  try {
    const res = await fetch(url, {
      headers: {
        "User-Agent": "Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/120.0.0.0 Safari/537.36"
      }
    });
    const json = await res.json();
    return json[0].map(item => item[0]).join("");
  } catch (err) {
    console.error(`Error translating text "${text}" to ${targetLang}:`, err);
    return text; // fallback to English
  }
}

async function translateObject(obj, targetLang, existingObj = {}) {
  const result = {};
  for (const key of Object.keys(obj)) {
    if (typeof obj[key] === "object") {
      result[key] = await translateObject(obj[key], targetLang, existingObj ? existingObj[key] : {});
    } else {
      if (existingObj && existingObj[key]) {
        result[key] = existingObj[key];
      } else {
        result[key] = await translateText(obj[key], targetLang);
      }
    }
  }
  return result;
}

async function main() {
  const localesDir = path.join(__dirname, "locales");

  // 1. Write English directly
  const enPath = path.join(localesDir, "en.json");
  const enData = JSON.parse(fs.readFileSync(enPath, "utf8"));
  enData.app = newStrings;
  fs.writeFileSync(enPath, JSON.stringify(enData, null, 2), "utf8");
  console.log("English locales updated successfully.");

  // 2. Translate and write others
  for (const lang of LANGUAGES) {
    console.log(`Translating keys to: ${lang}...`);
    const langPath = path.join(localesDir, `${lang}.json`);
    let existingApp = {};
    if (fs.existsSync(langPath)) {
      const langData = JSON.parse(fs.readFileSync(langPath, "utf8"));
      existingApp = langData.app || {};
    }
    const translatedApp = await translateObject(newStrings, lang, existingApp);
    if (fs.existsSync(langPath)) {
      const langData = JSON.parse(fs.readFileSync(langPath, "utf8"));
      langData.app = translatedApp;
      fs.writeFileSync(langPath, JSON.stringify(langData, null, 2), "utf8");
      console.log(`Locales for ${lang} updated successfully.`);
    } else {
      console.warn(`Locale file not found for: ${lang}`);
    }
  }
  console.log("All locales translated and saved successfully!");
}

main().catch(err => {
  console.error("Translation script failed:", err);
});
